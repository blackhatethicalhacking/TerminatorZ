#!/bin/bash
#############################################################################
# TerminatorZ v3.0 - Reporter Module
# Parses structured findings (target/poc/proof) into HTML and Markdown.
#############################################################################

generate_report() {
    local domain=$1
    tz_info "Generating reports..."
    generate_markdown_report "$domain"
    generate_html_report "$domain"
    tz_ok "Reports generated:"
    echo "    - $TZ_CURRENT_RESULT_PATH/report.md"
    echo "    - $TZ_CURRENT_RESULT_PATH/report.html"
}

# Parse findings file into structured blocks by severity.
# Findings file format (per finding):
#   [SEVERITY] Display Name
#     target: <url>
#     poc:    <url>
#     proof:  <text>
#
#   (blank line between findings)
# Emits a JSON-ish array to stdout: one finding per line
#   SEV|NAME|TARGET|POC|PROOF
_parse_findings() {
    local findings_file=$1
    [ ! -f "$findings_file" ] && return 0

    awk '
    BEGIN { sev=""; name=""; tgt=""; poc=""; prf="" }
    /^\[(CRITICAL|HIGH|MEDIUM|LOW)\]/ {
        if (sev != "") {
            print sev "|" name "|" tgt "|" poc "|" prf
        }
        line = $0
        # Portable extraction: strip leading "[" then split on "] "
        sub(/^\[/, "", line)
        idx = index(line, "] ")
        if (idx > 0) {
            sev  = substr(line, 1, idx - 1)
            name = substr(line, idx + 2)
        } else {
            sev  = ""
            name = line
        }
        tgt = ""; poc = ""; prf = ""
        next
    }
    /^[[:space:]]*target:/ { sub(/^[[:space:]]*target:[[:space:]]*/,"",$0); tgt=$0; next }
    /^[[:space:]]*poc:/    { sub(/^[[:space:]]*poc:[[:space:]]*/,"",$0);    poc=$0; next }
    /^[[:space:]]*proof:/  { sub(/^[[:space:]]*proof:[[:space:]]*/,"",$0);  prf=$0; next }
    END {
        if (sev != "") { print sev "|" name "|" tgt "|" poc "|" prf }
    }
    ' "$findings_file"
}

#############################################################################
# Markdown report
#############################################################################
generate_markdown_report() {
    local domain=$1
    local timestamp
    timestamp=$(date '+%Y-%m-%d %H:%M:%S')
    local findings_file="$TZ_CURRENT_RESULT_PATH/$domain.txt"
    local report_file="$TZ_CURRENT_RESULT_PATH/report.md"

    recompute_stats "$domain"

    {
        echo "# TerminatorZ v3.0 Security Report"
        echo
        echo "**Target:** $domain"
        echo "**Date:** $timestamp"
        echo "**Run:** $(basename "$TZ_CURRENT_RESULT_PATH")"
        echo "**Author:** Chris 'SaintDruG' Abou-Chabké"
        echo "**Organization:** Black Hat Ethical Hacking"
        echo "**Website:** https://www.blackhatethicalhacking.com"
        echo
        echo "---"
        echo
        echo "## Executive Summary"
        echo
        echo "| Metric | Count |"
        echo "|---|---|"
        echo "| Total URLs scanned | $TOTAL_URLS |"
        echo "| Critical | $VULN_CRITICAL |"
        echo "| High | $VULN_HIGH |"
        echo "| Medium | $VULN_MEDIUM |"
        echo "| Low | $VULN_LOW |"
        echo
        echo "---"
        echo
        echo "## Findings"
        echo

        local sev
        for sev in CRITICAL HIGH MEDIUM LOW; do
            echo "### $sev"
            echo
            local any=0
            local line
            while IFS='|' read -r s name tgt poc prf; do
                [ "$s" != "$sev" ] && continue
                any=1
                echo "**$name**"
                echo
                echo "- Target: \`$tgt\`"
                echo "- Proof-of-Concept: \`$poc\`"
                [ -n "$prf" ] && echo "- Proof: $prf"
                echo
            done < <(_parse_findings "$findings_file")
            [ $any -eq 0 ] && { echo "_None._"; echo; }
        done

        echo "---"
        echo
        echo "## Notes"
        echo
        echo "This report was generated for authorised security testing only."
        echo "Findings are passive-scan indicators and must be manually validated before reporting."
        echo
        echo "_Generated by TerminatorZ v3.0 — https://www.blackhatethicalhacking.com_"
        echo "_Author: Chris 'SaintDruG' Abou-Chabké_"
    } > "$report_file"
}

