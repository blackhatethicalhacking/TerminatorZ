# Produce one .finding block per parsed line at the requested severity
_findings_html_blocks() {
    local findings_file=$1
    local sev=$2
    if [ ! -f "$findings_file" ]; then
        echo '<div class="none">None.</div>'
        return
    fi

    local any=0
    local out=""
    local sev_lower
    sev_lower=$(echo "$sev" | tr '[:upper:]' '[:lower:]')

    while IFS='|' read -r s name tgt poc prf; do
        [ "$s" != "$sev" ] && continue
        any=1
        local name_e tgt_e poc_e prf_e
        name_e=$(_html_escape "$name")
        tgt_e=$(_html_escape  "$tgt")
        poc_e=$(_html_escape  "$poc")
        prf_e=$(_html_escape  "$prf")
        out+="<div class=\"finding ${sev_lower}\">"
        out+="<div class=\"finding-name\">${name_e}</div>"
        out+="<div class=\"finding-row\"><span class=\"label\">Target:</span> <span class=\"value\">${tgt_e}</span></div>"
        out+="<div class=\"finding-row\"><span class=\"label\">PoC:</span> <span class=\"value\">${poc_e}</span></div>"
        if [ -n "$prf" ]; then
            out+="<div class=\"finding-row\"><span class=\"label\">Proof:</span> <span class=\"value proof\">${prf_e}</span></div>"
        fi
        out+="</div>"
    done < <(_parse_findings "$findings_file")

    if [ $any -eq 0 ]; then
        echo '<div class="none">None.</div>'
    else
        echo "$out"
    fi
}

_html_escape() {
    local s=$1
    s=${s//&/&amp;}
    s=${s//</&lt;}
    s=${s//>/&gt;}
    s=${s//\"/&quot;}
    printf '%s' "$s"
}

export -f generate_report generate_markdown_report generate_html_report
